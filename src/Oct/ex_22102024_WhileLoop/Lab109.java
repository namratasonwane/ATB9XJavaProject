package Oct.ex_22102024_WhileLoop;

public class Lab109 {
    public static void main(String[] args)
    {
        int i =0;
        System.out.println("Values using while loop: ");
        while(i < 10)
        {
            System.out.println(i);
            i++;
        }

        System.out.println("Values using for loop: ");
        for(int j=0;j<10;j++)
        {
            System.out.println(j);
        }
    }
}
