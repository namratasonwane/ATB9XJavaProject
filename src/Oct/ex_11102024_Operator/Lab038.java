package Oct.ex_11102024_Operator;

public class Lab038 {
    public static void main(String[] args) {
        // Arithmetic Operators
//        + (Addition)
//                - (Subtraction)
//                * (Multiplication)
//                / (Division)
//                % (Modulo) | Modulus - Remainder
        int a = 20;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
